#!/usr/bin/env python3
"""Función concatenar"""

from typing import Tuple


def concat(str1: str, str2: str) -> str:

    """
    Define la función y concatena 2 números string
    y retorna el resultado

    Parameters:
    str1 (str): El primer string
    str2 (str): El segundo string

    Return:
    str: string concatenado
    """

    concatenated_string = str1 + str2
    return concatenated_string
