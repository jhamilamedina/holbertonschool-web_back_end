"""
Retorna la suma de 2 números flotantes
"""

from typing import Tuple


"""Define la función add"""


def add(a: float, b: float) -> float:
    """
    Función que toma 2 números floates y
    devuelve la suma como un número flotante
    """
    result: float = a + b
    return result
